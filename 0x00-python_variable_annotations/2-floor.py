#!/usr/bin/env python3
""" Write a type-annotated function floor which takes a float n as argument
    and returns the floor of the float.
"""
import math
""" the floor function from math modulev """


def floor(n: float) -> float:
    """ Write a type-annotated function floor which takes a float n as argument
        and returns the floor of the float.
    """
    return math.floor(n)
